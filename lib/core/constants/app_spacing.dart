  AppSpacing._(); // Private constructor to prevent instantiation

  /// Extra small spacing - 4px
  static const double xs = 4.0;

  /// Small spacing - 8px
  static const double sm = 8.0;

  /// Medium spacing - 12px
  static const double md = 12.0;

  /// Large spacing - 16px
  static const double lg = 16.0;

  /// Extra large spacing - 20px
  static const double xl = 20.0;

  /// Double extra large spacing - 24px
  static const double xxl = 24.0;

  /// Triple extra large spacing - 32px
  static const double xxxl = 32.0;

  /// Quadruple extra large spacing - 40px
  static const double xxxxl = 40.0;

  /// Huge spacing - 48px
  static const double huge = 48.0;

  /// Component-specific spacing
  static const double appBarHeight = 56.0;
  static const double bottomNavHeight = 80.0;
  static const double fabSize = 56.0;
  static const double iconSize = 24.0;
  static const double iconSizeLarge = 32.0;
  static const double iconSizeSmall = 16.0;

  /// Form spacing
  static const double fieldSpacing = 16.0;
  static const double sectionSpacing = 24.0;
  static const double pageMargin = 16.0;

  /// Card spacing
  static const double cardPadding = 16.0;
  static const double cardMargin = 8.0;
  static const double cardSpacing = 12.0;
}
